function autoNext(input, nextId) {
    if (input.value.length >= input.maxLength) {
        document.getElementById(nextId).focus(); //setelah digit NIR terpenuhi, langsung enter ke tanggal lahir
    }
}

function nextTanggal(current, nextId) {
    if (current.value.length === current.maxLength) {
        document.getElementById(nextId).focus();
    }
}

function cekEnter(event, nextId) { //setelah NIR dan tanggal terpenuhi, bisa cek hasil dengan tekan enter
    if (event.key === "Enter") {
        event.preventDefault();
        const currentInput = event.target.value.trim();
        if (currentInput !== "") {
            if (nextId === "submit") {
                cekHasil();
            } else {
                document.getElementById(nextId).focus();
            }
        }
    }
}

function cekHasil() { //fungsi cek hasil menggunakan ID di HTML
    const nisn = document.getElementById("nisn").value.trim();
    const hari = document.getElementById("tanggal-hari").value.padStart(2, "0");
    const bulan = document.getElementById("tanggal-bulan").value.padStart(2, "0");
    const tahun = document.getElementById("tanggal-tahun").value.trim();
    const tanggal = `${hari}-${bulan}-${tahun}`;

    const peserta = data.find(item => item.nisn === nisn && item.tanggal === tanggal);

    if (peserta) {
        if (peserta.status === "LOLOS") {
            window.location.href =`lolos.html?nisn=${encodeURIComponent(peserta.nisn)}&tanggal=${encodeURIComponent(peserta.tanggal)}&nama=${encodeURIComponent(peserta.nama)}&divisi=${encodeURIComponent(peserta.divisi)}`;
        } else {
            window.location.href =`tidaklolos.html?nisn=${encodeURIComponent(peserta.nisn)}&tanggal=${encodeURIComponent(peserta.tanggal)}&nama=${encodeURIComponent(peserta.nama)}`;
        }
    } else {
        alert("data tidak ditemukan. Silahkan periksa kembali NRI dan Tanggal Lahir");
    }
}
